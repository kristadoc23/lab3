import turtle



def shape_turtle(x):
    fred = turtle.Turtle()
    fred.color(x)
    fred.forward(100)
    fred.right(90)
    fred.forward(100)
    fred.right(90)
    fred.forward(100)
    fred.right(90)
    fred.forward(100)

shape_turtle("green")
shape_turtle("blue")
shape_turtle("gold")
